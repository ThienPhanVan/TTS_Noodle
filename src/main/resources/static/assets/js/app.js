class User{
    constructor(id, fullName, roleId, phone, email, address, status, avatarUrl, username) {
        this.id = id;
        this.fullName = fullName;
        this.roleId = roleId;
        this.phone = phone;
        this.email = email;
        this.address = address;
        this.status = status;
        this.avatarUrl = avatarUrl;
        this.username = username;
    }
}